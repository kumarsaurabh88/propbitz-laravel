        }
        onTabClick(event, element) {
            event.preventDefault();
            let targetElement = $(element.attr('href'));
            if (targetElement.length) {
                let scrollTop = targetElement.offset().top - this.tabContainerHeight + 1;
                $('html, body').animate({
                    scrollTop: scrollTop
                }, 600);
            }
        }
        onScroll() {
            this.checkTabContainerPosition();
            this.findCurrentTabSelector();
        }
        onResize() {
            if (this.currentId) {
                this.setSliderCss();
            }
        }
        checkTabContainerPosition() {
            let offset = $('.et-hero-tabs').offset().top + $('.et-hero-tabs').height() - this.tabContainerHeight;
            if ($(window).scrollTop() > offset) {
                $('.et-hero-tabs-container').addClass('et-hero-tabs-container--top');
            } else {
                $('.et-hero-tabs-container').removeClass('et-hero-tabs-container--top');
            }
        }
        findCurrentTabSelector() {
            let newCurrentId = null;
            let newCurrentTab = null;
            let self = this;
            $('.et-hero-tab').each(function() {
                let id = $(this).attr('href');
                let targetElement = $(id);
                if (targetElement.length) {
                    let offsetTop = targetElement.offset().top - self.tabContainerHeight;
                    let offsetBottom = targetElement.offset().top + targetElement.height() - self
                        .tabContainerHeight;
                    if ($(window).scrollTop() > offsetTop && $(window).scrollTop() < offsetBottom) {
                        newCurrentId = id;
                        newCurrentTab = $(this);
                    }
                }
            });
            if (this.currentId != newCurrentId || this.currentId === null) {
                if (this.currentTab) {
                    this.currentTab.removeClass('active');
                }
                this.currentId = newCurrentId;
                this.currentTab = newCurrentTab;
                if (this.currentTab) {
                    this.currentTab.addClass('active');
                }
                this.setSliderCss();
            }
        }
        setSliderCss() {
            let width = 0;
            let left = 0;
            if (this.currentTab) {
                width = this.currentTab.css('width');
                left = this.currentTab.offset().left;
            }
            $('.et-hero-tab-slider').css('width', width);
            $('.et-hero-tab-slider').css('left', left);
        }
    }
    new StickyNavigation();
</script>
